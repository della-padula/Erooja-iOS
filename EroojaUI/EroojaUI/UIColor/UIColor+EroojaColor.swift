import UIKit

public extension UIColor {
    class var EROOJAMAIN100: UIColor {
        return UIColor(rgb: 0xF3590F)
    }
    
    class var EROOJAGRAY33: UIColor {
        return UIColor(rgb: 0xD8D8D8)
    }
    
    class var EROOJAGRAYD8: UIColor {
        return UIColor(rgb: 0xD8D8D8)
    }
    
    class var EROOJAGRAY75: UIColor {
        return UIColor(rgb: 0x757575)
    }
}
